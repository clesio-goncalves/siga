package siga.capau.modelo;

public class FiltroTurma {

	private String curso;
	private String ano_ingresso;
	private String periodo_ingresso;
	private String tipo_turma;
	private String situacao;

	public String getCurso() {
		return curso;
	}

	public void setCurso(String curso) {
		this.curso = curso;
	}

	public String getAno_ingresso() {
		return ano_ingresso;
	}

	public void setAno_ingresso(String ano_ingresso) {
		this.ano_ingresso = ano_ingresso;
	}

	public String getPeriodo_ingresso() {
		return periodo_ingresso;
	}

	public void setPeriodo_ingresso(String periodo_ingresso) {
		this.periodo_ingresso = periodo_ingresso;
	}

	public String getTipo_turma() {
		return tipo_turma;
	}

	public void setTipo_turma(String tipo_turma) {
		this.tipo_turma = tipo_turma;
	}

	public String getSituacao() {
		return situacao;
	}

	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}

}
